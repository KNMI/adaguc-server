#include <string>

/**
 * Check if unit is a knot
 * @param units
 * @returns true if matches
 */
bool isKnotsUnit(const std::string &units);

/**
 * Check if unit is a degree
 * @param units
 * @returns true if matches
 */
bool isDegreesUnit(const std::string &units);

/**
 * Check if unit is mps (meter per second)
 * @param units
 * @returns true if matches
 */
bool isMpsUnit(const std::string &units);
